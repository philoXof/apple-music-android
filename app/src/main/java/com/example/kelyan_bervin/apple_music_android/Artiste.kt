package com.example.kelyan_bervin.apple_music_android

class Artiste constructor(name: String, localisation: String, description: String, albums: List<Album>, tracks: List<Track>){
     fun getName(): String{
         return this.getName();
     }

    fun getLocalisation(): String{
        return this.getLocalisation();
    }

    fun getDescription(): String{
        return this.getDescription();
    }

    fun getAlbums(): List<Album>{
        return this.getAlbums();
    }

    fun getTracks(): List<Track>{
        return this.getTracks();
    }

}
